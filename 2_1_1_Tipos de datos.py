# Para saber que tipos de datos tenemos se usa el comando type.

#Los tipos de datos básicos son:

# Tipos numéricos: int, float.
# - int: nmúmeros enteros (positivos y negativos)
print(type(42))
print(type(-42))
# - float: números decimales (positivos y negativos). Todos los número que tienen un punto se consideran float aunque
        # el decimal sea cero.
print(type(42.0))
print(type(-42.05))

# Tipos de texto: str.
# - str (string) representa secuencias o cadenas de caracteres. Siempre empiezan y terminan
        # con comillas dobles o simples
print(type("hola mundo"))
print(type("2 + 2 = 5"))

# Tipos lógicos (booleanos): bool.
# -bool reprtesenta valores booleanos o de lógica binaria: verdadero o falso. True False.
# - La primera letra va siempre en mayúsculas, si no Python no lo reconocera.
print(type(False))
print(type(True))







