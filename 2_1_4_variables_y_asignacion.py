# Para recordar los valores y usarlos posteriormente les asignamos un nombre. Estos nombres son variables.
# Python usará el último valor que almacenemos con ese nombre.
print((19 - 14) * 200)
# Hora de salida, menos hora de llegada por valor de hora de estacionamiento.
llegada = 14
# = asinga un valor a una variable
print((19 - llegada) * 200)
# no se ponen espacios en los nombres de las variables.
pesos_por_hora = 200
print((19 - llegada) * pesos_por_hora)
salida = 19
print((salida - llegada) * pesos_por_hora)

llegada = 20
salida = 22
print((salida - llegada) * pesos_por_hora)

r = 5
pi = 3.1416
s = "El área es: "
area = pi * r ** 2
print(s + str(area))

r = 12.8
area = pi * r ** 2
print(s + str(area))

