#Como hacer que el programa decida

# Instrucción condicional: if sin else
# Si la condición es verdadera el programa ejecuta una acción, si es falsa no hace nada.

charco = True
print("Comienza la caminata")
if charco == True:
    print("A saltar")
print("Fin de la caminata")

charco = False
print("Comienza la caminata")
if charco == True:
    print("A saltar")
print("Fin de la caminata")
# else equivale a decir en caso contrario y nunca puede ir sin if

